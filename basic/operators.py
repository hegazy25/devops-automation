print (10+5)  #addition
print (10-5)  #subtraction
print (10*5)  #multiplication
print (10/5)  #normal division (float)
print (10%5)  #remainder of division
print (10**2)  #exponentiation
print (1000//2)   #round down division


print (10==5)   #equality
print (10!=5)    #not equal
print (10>5)    #greater than
print (10<5)   #less than
print (10>=5)   #greater than or equal to
print (10<=5)   #less than or equal to

print(10>5 and 10<20)   #logical operators and
print(10>5 or 10<20)    #logical operators or
print(not 10>5)         #logical operators not

print(10 is 5)          #identity operators is
print(10 is not 5)      #identity operators is not

print(10 in [1,2,3,4,5])        #membership operators in
print(10 not in [1,2,3,4,5])    #membership operators not in

print(bin(10))                  #convert to binary
print(int('1010', 2))           #convert from binary to decimal
print(hex(255))                 #convert to hexadecimal
